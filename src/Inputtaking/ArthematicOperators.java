package Inputtaking;

public class ArthematicOperators {
    /*
    Arthematic operators are used to perform arithematic operations on variables
    and values.*/


    public static void main(String[] args) {

        int a = 4;
        int b = 4;

        System.out.println(a-b);
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a/b); // if a and b are interger then resulting quotient is also integer.
        // if one of the number is float then output will be float.
        System.out.println(a%b);

    }
}
