package Inputtaking;

public class UnaryOperators {

    public static void main(String[] args) {
        // Unary operators are used with one operand . for eg : ++ ,--
        int num = 5;
        System.out.println(++num);

        int a = 12, b = 12;
        int res1 , res2;
        System.out.println(a);

        res1 = ++a;
        System.out.println(res1);

        res2 = --b;
        System.out.println(res2);

        // + : unary plus
        // - : unary minus
        // 47.2
        
    }
}
